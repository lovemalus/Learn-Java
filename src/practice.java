public class practice {

    public static void main(String[] args){
        Blastoff();
    }

    //This is a Blastoff gadget.
    public static void Blastoff(){
        for (int i = 10; i > 0; i--)
            System.out.println("Count down ... " + i);
        System.out.println("Blastoff!");
    }

}